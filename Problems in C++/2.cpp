#include<iostream>
using namespace std;

int main(){
    int a,b;
    cout<<"Enter Numbers:";
    cin>>a>>b;
    cout<<"The Sum of "<<a<<" and "<<b<<" is "<<a+b<<endl;
    cout<<"The net of "<<a<<" and "<<b<<" is "<<a-b<<endl;
    cout<<"The product of "<<a<<" and "<<b<<" is "<<a*b<<endl;
    cout<<"The quotient when "<<a<<" divided by "<<b<<" is "<<a/b<<endl;
    cout<<"The remainder when "<<a<<" divided by "<<b<<" is "<<a%b<<endl;
    return 0;
}
